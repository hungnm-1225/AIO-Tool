interface I18nContextType {
  lang: Language;
  setLang: (lang: Language) => void;
  t: (keyPath: string) => string;
}

const I18nContext = createContext<I18nContextType>({
  lang: "vi",
  setLang: () => {},
  t: (keyPath: string) => keyPath,
});

export function I18nProvider({ children, initialLang = "vi" }: { children: ReactNode; initialLang?: Language }) {
  const [lang, setLangState] = useState<Language>(() => {
    try {
      const stored = localStorage.getItem("vibe_code_lang");
      if (stored === "vi" || stored === "en") return stored;
    } catch (e) {
      console.warn("Could not read stored language", e);
    }
    return initialLang;
  });

  const setLang = (newLang: Language) => {
    setLangState(newLang);
    try {
      localStorage.setItem("vibe_code_lang", newLang);
    } catch (e) {
      console.warn("Could not store language preference", e);
    }
  };

  const t = (keyPath: string): string => {
    const parts = keyPath.split(".");
    let curr: any = translations[lang];
    for (const p of parts) {
      if (curr && curr[p] !== undefined) {
        curr = curr[p];
      } else {
        // Fallback to English if key missing in current language
        let fallback: any = translations["en"];
        for (const fp of parts) {
          if (fallback && fallback[fp] !== undefined) {
            fallback = fallback[fp];
          } else {
            return keyPath;
          }
        }
        return typeof fallback === "string" ? fallback : keyPath;
      }
    }
    return typeof curr === "string" ? curr : keyPath;
  };

  return (
    <I18nContext.Provider value={{ lang, setLang, t }}>
      {children}
    </I18nContext.Provider>
  );
}

export function useI18n() {
  return useContext(I18nContext);
}
